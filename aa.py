def select_bills(month, bills):
    if month % 3 == 0:
        # Если месяц кратен 3, оплачиваем все счета, кроме первого и последнего
        return bills[1:-1]
    else:
        # В остальные месяцы оплачиваем только первый и последний счёт
        return [bills[0], bills[-1]]

# Пример использования:
bills = ['Оплата сервера', 'Горячая вода', 'Электричество', 'Налог на кота']
month = 3  # Март
print(select_bills(month, bills))  # Вывод: ['Горячая вода', 'Электричество']

month = 4  # Апрель
print(select_bills(month, bills))  # Вывод: ['Оплата сервера', 'Налог на кота'] 